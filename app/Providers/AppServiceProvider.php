    /**
     * Bootstrap any application services.
     *
     * @return void
     */
    public function boot()
    {
        $agent_img = new Agent();

        $agent = '';

        if (!$agent_img->isDesktop() && !$agent_img->isTablet()) {
            $agent = '_medium';
        }

        Paginator::useBootstrap();

        $lang = str_replace('_', '-', app()->getLocale());

        $translate = $this->converter;

        View::share(compact(['lang' , 'translate', 'agent']));
    }


}
